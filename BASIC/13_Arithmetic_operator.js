// Arithmetic operators
// An arithmetic operator takes numerical values (either literals or variables) as their operands and returns a single numerical value.
//  The standard arithmetic operators are addition (+), subtraction (-), multiplication (*), and division (/). These operators work as they do in most other programming languages when used with floating point numbers (in particular, note that division by zero produces Infinity).
//( 
//      +       // Binary operator. Returns the integer addition of the two operands.
//      -       // Binary operator. Returns the integer substraction of the two operands.
//      *       // Binary operator. Returns the integer multiplication of the two operands.
//      /       // Binary operator. Returns the integer quotient of dividing the two operands.
//      %       // Binary operator. Returns the integer remainder of dividing the two operands.
//      Exponentiation operator (**)            // Calculates the base to the exponent power, that is, base^exponent
//) 

let a = 20;
let b = 10;
console.log(a+b);
console.log(a-b);
console.log(a*b);
console.log(a/b);
console.log(a%b);
a=2;b=3;
console.log(a**b);