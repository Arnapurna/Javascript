// Unary operators

// A unary operation is an operation with only one operand.
//(
//    Increment (++)
//    Decrement (--)
//    Not (!)
//)

// By using unary operator we can increment or decrement any variable by 1.
// variable++/-- (Post inc/dec)
// ++/--variable (Pre inc/dec)

let a = 0;
a++;
console.log(a);

let b = 0;
b = b++;
console.log(b);

let c = 0;
++c;
console.log(c);

let d = 0;
d = ++d;
console.log(d);

console.log(!true);
console.log(!false);